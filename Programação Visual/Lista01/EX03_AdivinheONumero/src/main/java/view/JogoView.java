    private String verifyShot(int tip){
        
        startButton.setEnabled(endGame());
        System.out.println("Attempt");
        String message = "";
        
        if(tip >= 1){
            message = "Bigger";
            jogo.loseLifePoints();
        }else if(tip <= -1){
            message = "Smaller";
            jogo.loseLifePoints();
        }else if(tip == 0){
            JOptionPane.showMessageDialog(this, "YOU WIN!!!", "CONGRATULATIONS", JOptionPane.INFORMATION_MESSAGE);
        }
        lifePoints.setText(String.valueOf(jogo.getLifePoints()));
        return message;
        
        
    }
    private boolean endGame(){
        if(jogo.getLifePoints() == 1){
            JOptionPane.showMessageDialog(this,"YOU LOSE", "GAME OVER", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        return true;
    }
            

