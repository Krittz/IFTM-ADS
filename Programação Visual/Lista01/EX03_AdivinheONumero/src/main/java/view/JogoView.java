    private void startButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startButtonActionPerformed
       
        if(easyButton.isSelected()){
           jogo.setDificultyEasy();
           jogo.startGame();
           lifePoints.setText(String.valueOf(jogo.getLifePoints()));
       }else if(midButton.isSelected()){
           jogo.setDificultyMedium();
           jogo.startGame();
           lifePoints.setText(String.valueOf(jogo.getLifePoints()));
       }else if(hardButton.isSelected()){
           jogo.setDificultyHard();
           jogo.startGame();
           lifePoints.setText(String.valueOf(jogo.getLifePoints()));
       }else{
           JOptionPane.showMessageDialog(this,"CHOOSE DIFFICULTY", "ERROR", JOptionPane.WARNING_MESSAGE);
       }
       shotButton.setEnabled(endGame());
    }//GEN-LAST:event_startButtonActionPerformed

    private void shotButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_shotButtonActionPerformed
       try{
           if(jogo.getLifePoints() <=1){              
               shotButton.setEnabled(endGame());
           }
           int shot = Integer.parseInt(shotInput.getText());
           int tip = jogo.compareBet(shot);
           
           String message = verifyShot(tip);
           dica.setText(message);
       }catch(Exception e){
           JOptionPane.showMessageDialog(this,"Only numbers", "ERROR", JOptionPane.INFORMATION_MESSAGE);
       }
    }//GEN-LAST:event_shotButtonActionPerformed

